double trimMean(List<int> arr) {
  arr.sort();
  double meanListLength = ((arr.length / 100) * 90).roundToDouble();
  while (arr.length > meanListLength) {
    arr = arr.sublist(1, arr.length - 1);
  }
  double mean = 0;
  for (int number in arr) {
    mean += number;
  }
  return mean / arr.length;
}

